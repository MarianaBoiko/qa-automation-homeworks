const fruitCode = 2;

switch (fruitCode) {
    case 1:
        console.log('Fruit: Apple');
        break;
    case 2:
        console.log('Fruit: Banana');
        break;
    case 3:
        console.log('Fruit: Cherry');
        break;
    case 4:
        console.log('Fruit: Date');
        break;
    default:
        console.log('Unknown fruit');
}
